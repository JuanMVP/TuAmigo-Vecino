# Funcion que devuelve los Dnis del fichero BOE con numeros y letras correctos.
def getDnisCorrectos():
    # Array de dni's encontrados. Se instancia vacio, se rellena a continuacion.
    dniEncontradoList = []
    # Bucle que abre el archivo y lo recorre linea a linea. La variable i es solo para la fase desarrollo ver la linea en la que ha encontrado el dni.
    for i, linea in enumerate(open("./reto2-BOE.txt")):
        # Bucle que recorre cada linea y devuelve las cadenas de caracteres que cumplen la expresion regular.
        for match in re.finditer(dniRegExp, linea):
            # A la variable dniEncontrado se le asocia el valor de la expresion regular. Se especifica el grupo para que no devuelva otros caracteres no deseados (comas y parentesis).
            dniEncontrado = match.group(1)
            # A la variable numsDniEncontrado se le asocia la subcadena de caracteres que contiene los numeros del dni.
            numsDniEncontrado = dniEncontrado[:-1]
            # print(numsDniEncontrado)
            # A la variable letraDniEncontrado se le asocia la subcadena de caracteres que contiene la letra del dni.
            letraDniEncontrado = dniEncontrado[-1:]
            # print(letraDniEncontrado)
            # Si la letra del dni encontrado es igual a la letra que devuelve el calculo, agrega el dni al array de dni's encontrados.
            if letraDniEncontrado == calcularLetraDni(numsDniEncontrado):
                dniEncontradoList.append(dniEncontrado)
            # print("DNI %s en linea %s" % (dniEncontrado, i+1))
    
    return dniEncontradoList

